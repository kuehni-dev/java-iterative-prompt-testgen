WITH STEP1 AS (
    SELECT I.*,
           R.ID                 RUN_ID,
           R.D4J_PROJECT,
           R.D4J_BUG_ID,
           FI.FEEDBACK_LOOP_ID,
           (SELECT TOP 1 FOLLOW_UP_PROMPT_ID
            FROM FIXER_STAGE S
            WHERE S.ITERATION_ID = I.ID
              AND S.FOLLOW_UP_PROMPT_ID IS NOT NULL
            ORDER BY S.ID DESC) FIXER_PROMPT_ID
    FROM ITERATION I
             JOIN TARGET T ON I.TARGET_ID = T.ID
             JOIN RUN R ON T.RUN_ID = R.ID
             LEFT JOIN FEEDBACK_LOOP_ITERATION FI ON I.ID = FI.ITERATION_ID),
     STEP2 AS (
    SELECT *,
           COALESCE(FIXER_PROMPT_ID, GENERATION_PROMPT_ID) FINAL_PROMPT_ID
    FROM STEP1)
SELECT STEP2.*,
       M.TEXT TEST_SOURCE_CODE
FROM STEP2
         LEFT JOIN PROMPT P ON P.ID = FINAL_PROMPT_ID
         LEFT JOIN PROMPT_MESSAGE M ON P.ID = M.PROMPT_ID AND M.ROLE = 'ASSISTANT';
