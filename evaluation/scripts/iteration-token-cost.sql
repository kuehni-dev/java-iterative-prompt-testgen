WITH STEP1 AS (
    SELECT R.UUID                                                                RUN_UUID,
           T.CLASS_NAME,
           I.ID,
           I.NUMBER,
           I.GENERATION_PROMPT_ID,
           COALESCE(CASE WHEN I.NUMBER = 0 THEN NULL ELSE F.TYPE END, 'INITIAL') FEEDBACK_TYPE
    FROM ITERATION I
             LEFT JOIN FEEDBACK_LOOP_ITERATION FI ON I.ID = FI.ITERATION_ID
             LEFT JOIN FEEDBACK_LOOP F ON FI.FEEDBACK_LOOP_ID = F.ID
             JOIN TARGET T ON I.TARGET_ID = T.ID
             JOIN RUN R ON T.RUN_ID = R.ID
    WHERE R.COMPLETED_AT IS NOT NULL
    GROUP BY I.ID, FEEDBACK_TYPE)
SELECT STEP1.RUN_UUID,
       STEP1.CLASS_NAME,
       STEP1.FEEDBACK_TYPE,
       STEP1.NUMBER,
       COALESCE(GEN_P.INPUT_TOKENS, 0) + COALESCE(SUM(FOLLOWUP_P.INPUT_TOKENS), 0)   INPUT_TOKEN_COUNT,
       COALESCE(GEN_P.OUTPUT_TOKENS, 0) + COALESCE(SUM(FOLLOWUP_P.OUTPUT_TOKENS), 0) OUTPUT_TOKEN_COUNT
FROM STEP1
         LEFT JOIN FIXER_STAGE S ON STEP1.ID = S.ITERATION_ID
         LEFT JOIN PROMPT GEN_P ON STEP1.GENERATION_PROMPT_ID = GEN_P.ID
         LEFT JOIN PROMPT FOLLOWUP_P ON S.FOLLOW_UP_PROMPT_ID = FOLLOWUP_P.ID
GROUP BY RUN_UUID, CLASS_NAME, FEEDBACK_TYPE, STEP1.NUMBER;
